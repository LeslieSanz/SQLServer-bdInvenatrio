    private void mostrarListaProveedor(){
        modelo.setRowCount(0);
         for (Proveedor prov : provd.lisProveedor()) {
             Object v[]={
                 prov.getCodprov(),
                 prov.getNombre(),
                 prov.getTelf(),
                 prov.getDireccion(),
             };
             modelo.addRow(v);
         }
    }
